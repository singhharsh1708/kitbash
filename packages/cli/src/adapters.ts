/**
 * Launch adapters: claude-code, cursor, agentsmd (the floor).
 * Conformance obligations (spec §2, §7): budgets enforced by the caller,
 * degradation visible, generated-file headers mandatory.
 */

import { existsSync, readFileSync } from "node:fs";
import { join } from "node:path";
import { estimateTokens, standingStub, type LoadedSkill } from "./ksf.js";

export interface CompiledFile {
  path: string; // repo-relative
  content: string;
}

export interface AdapterOutput {
  files: CompiledFile[];
  warnings: string[];
}

export interface Adapter {
  id: string;
  /** Capabilities this target supports, matched against targets.requires. */
  capabilities: string[];
  detect(root: string): boolean;
  emit(skill: LoadedSkill, body: string, root: string): AdapterOutput;
}

function header(skill: LoadedSkill): string {
  const { name, version } = skill.manifest.skill;
  return `<!-- generated by kitbash — do not edit; source: .kitbash/skills/${name} @ ${version} -->`;
}

function degradationWarnings(skill: LoadedSkill, adapter: Adapter): string[] {
  const missing = skill.manifest.targets.requires.filter((r) => !adapter.capabilities.includes(r));
  return missing.map(
    (cap) => `${skill.manifest.skill.name} → ${adapter.id}: target lacks "${cap}"; compiled instruction-only (degraded)`,
  );
}

const claudeCode: Adapter = {
  id: "claude-code",
  capabilities: ["scripts", "hooks", "subagents"],
  detect: (root) => existsSync(join(root, ".claude")),
  emit(skill, body) {
    const { name, description } = skill.manifest.skill;
    const content = `---\nname: ${name}\ndescription: ${description}\n---\n${header(skill)}\n\n${body}`;
    return { files: [{ path: `.claude/skills/${name}/SKILL.md`, content }], warnings: degradationWarnings(skill, this) };
  },
};

const cursor: Adapter = {
  id: "cursor",
  capabilities: [],
  detect: (root) => existsSync(join(root, ".cursor")),
  emit(skill, body) {
    const { name, description } = skill.manifest.skill;
    const content = `---\ndescription: ${description}\nalwaysApply: false\n---\n${header(skill)}\n\n${body}`;
    return { files: [{ path: `.cursor/rules/${name}.mdc`, content }], warnings: degradationWarnings(skill, this) };
  },
};

export const AGENTS_MD = "AGENTS.md";

function markers(name: string): { begin: string; end: string } {
  return { begin: `<!-- kitbash:begin ${name} -->`, end: `<!-- kitbash:end ${name} -->` };
}

/** The floor: everything that reads AGENTS.md (Codex, Gemini CLI, many others). */
const agentsmd: Adapter = {
  id: "agentsmd",
  capabilities: [],
  detect: () => true,
  emit(skill, body) {
    const { name } = skill.manifest.skill;
    const { begin, end } = markers(name);
    const section = `${begin}\n${header(skill)}\n\n## Skill: ${name}\n\n${body.trim()}\n${end}`;
    const warnings = degradationWarnings(skill, this);
    const cost = estimateTokens(body);
    const standing = estimateTokens(standingStub(body));
    if (skill.manifest.context.disclosure === "lazy") {
      warnings.push(
        `${name} → agentsmd: target cannot lazy-load; §${name} costs ~${cost} tokens standing (declared standing: ${standing})`,
      );
    }
    return { files: [{ path: AGENTS_MD, content: section }], warnings };
  },
};

export const ADAPTERS: Adapter[] = [claudeCode, cursor, agentsmd];

/** Replace or append a skill's marker-delimited section in AGENTS.md content. */
export function mergeAgentsSection(existing: string, name: string, section: string): string {
  const { begin, end } = markers(name);
  const re = new RegExp(`${escapeRe(begin)}[\\s\\S]*?${escapeRe(end)}`);
  if (re.test(existing)) return existing.replace(re, section);
  const sep = existing.trim().length ? `${existing.replace(/\s*$/, "")}\n\n` : "";
  return `${sep}${section}\n`;
}

export function readAgentsMd(root: string): string {
  const p = join(root, AGENTS_MD);
  return existsSync(p) ? readFileSync(p, "utf8") : "";
}

function escapeRe(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
